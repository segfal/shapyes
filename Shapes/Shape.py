


class Shape:

    def __init__(self,x,y,z):
        self.x, self.y, self.z = x, y, z
    
    def getx(self):
        return self.x
    
    def gety(self):
        return self.y
    
    def getz(self):
        return self.z
    
    def setx(self, x):
        self.x = x
    
    def sety(self, y):
        self.y = y
    
    def setz(self, z):
        self.z = z
    
    
